#!/bin/sh
# Exit immediately if a command exits with a non-zero status.
set -e

# ==============================================================================
# 디버깅: 컨테이너에 주입된 모든 환경 변수를 출력합니다.
# 이 로그를 통해 Railway가 변수를 제대로 전달하는지 확인할 수 있습니다.
# ==============================================================================
echo "========================================================"
echo ">>>>>      Railway 환경 변수 주입 상태 확인      <<<<<"
echo "========================================================"
printenv | sort
echo "--------------------------------------------------------"
echo ">>>>> TAVUS 변수 직접 확인"
echo "TAVUS_API_KEY: $(printenv | grep TAVUS_API_KEY || echo 'NOT FOUND')"
echo "TAVUS_PERSONA_ID: $(printenv | grep TAVUS_PERSONA_ID || echo 'NOT FOUND')"
echo "========================================================"

# 원래 실행하려 했던 CMD 명령어를 실행합니다.
exec "$@"
